// ─── Helpers ────────────────────────────────────────────────────────

/** Get meetings for a specific state */
export function getYPAAMeetingsByState(state: string): YPAAMeeting[] {
  return YPAA_MEETINGS.filter((m) => m.state === state)
}

/** Get meeting counts by state — { CT: 22, NY: 30, ... } */
export function getYPAAMeetingCountsByState(): Record<string, number> {
  const counts: Record<string, number> = {}
  for (const m of YPAA_MEETINGS) {
    counts[m.state] = (counts[m.state] || 0) + 1
  }
  return counts
}

/** Get all unique states that have meetings */
export function getYPAAStatesWithMeetings(): string[] {
  return [...new Set(YPAA_MEETINGS.map((m) => m.state))].sort()
}

/** Get all unique days across meetings */
export function getYPAAUniqueDays(): string[] {
  const standardDays = ["Sunday", "Monday", "Tuesday", "Wednesday", "Thursday", "Friday", "Saturday"]
  const days = new Set(YPAA_MEETINGS.map((m) => m.day).filter(Boolean))
  return standardDays.filter((d) => days.has(d))
}

/** Total meeting count */
export const YPAA_MEETING_COUNT = YPAA_MEETINGS.length
